    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Empty Field");

        } else {
            // load the saved network 
            NeuralNetwork neuralNetwork = NeuralNetwork.createFromFile("desercion_escolar_multilayer.nnet");

            // set network input
            int AA = -1;
            int FO = 0;
            int NAA = 0;
            int ECS = -1;
            int NAC = -1;
            int F = -1;
            int EMS = -1;

            if (rbtAA1.isSelected()) {
                AA = -1;
            } else if (rbtAA2.isSelected()) {
                AA = 0;
            } else {
                AA = 1;
            }

            if (rbtNoFO.isSelected()) {
                FO = 0;
            } else {
                FO = 1;
            }

            if (rbtNAA1.isSelected()) {
                NAA = 0;
            } else {
                NAA = 1;
            }

            if (rbtDeficientEconS.isSelected()) {
                ECS = -1;
            } else if (rbtRegularEconS.isSelected()) {
                ECS = 0;
            } else if (rbtGoodEconS.isSelected()) {
                ECS = 1;
            }

            if (rbtNAC1.isSelected()) {
                NAC = -1;
            } else if (rbtNAC2.isSelected()) {
                NAC = 0;
            } else if (rbtNAC3.isSelected()) {
                NAC = 1;
            }

            if (rbtDeficientF.isSelected()) {
                F = -1;
            } else if (rbtRegularF.isSelected()) {
                F = 0;
            } else if (rbtGoodF.isSelected()) {
                F = 1;
            }

            if (rbtDeficientES.isSelected()) {
                EMS = -1;
            } else if (rbtRegularES.isSelected()) {
                EMS = 0;
            } else if (rbtGoodES.isSelected()) {
                EMS = 1;
            }

            neuralNetwork.setInput(AA, FO, NAA, ECS, NAC, F, EMS);

            // calculate network 
            neuralNetwork.calculate();

            // get network output 
            double[] networkOutput = neuralNetwork.getOutput();
            int salida1 = (int) (networkOutput[0] + 0.5);

            if (salida1 == 0) {
                txtResultado.setText(txtNombre.getText() + " LOW RISK!!");
            } else {
                txtResultado.setText(txtNombre.getText() + " HIGH RISK!!");
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

